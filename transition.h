#ifndef _TRANSITION_H_	// このマクロ定義がされてなかったら
#define _TRANSITION_H_	// ２重インクルード防止のマクロ定義

//==================================================
// 列挙型
//==================================================
typedef enum
{
	FADE_NONE = 0,	// 何もしていない状態
	FADE_OUT,		// フェードアウト(見えない)
	FADE_IN,		// フェードイン(見える)
	FADE_MAX
}TRANSITION;

//==================================================
// プロトタイプ宣言
//==================================================
//--------------------------------------------------
// 初期化
//--------------------------------------------------
void InitTransition(void);

//--------------------------------------------------
// 終了
//--------------------------------------------------
void UninitTransition(void);

//--------------------------------------------------
// 更新
//--------------------------------------------------
void UpdateTransition(void);

//--------------------------------------------------
// 描画
//--------------------------------------------------
void DrawTransition(void);

//--------------------------------------------------
// 設定(OUT)
//--------------------------------------------------
void SetTransitionOut(void);

//--------------------------------------------------
// 設定(OUT)
//--------------------------------------------------
void SetTransitionIn(void);

//--------------------------------------------------
// 取得
// 返値 : TRANSITION / 列挙型 遷移状態
//--------------------------------------------------
TRANSITION GetTransition(void);

#endif // !_TRANSITION_H_
